<h2>Encik Anuar Bin Ahmad (Pengarah Pusanika-UKM)</h2>
<p>Pengetua kolej Ibu Zain</p>


<p>Encik Anuar Bin Ahmad merupakan pensyarah Fakulti Pendidikan UKM telah dilantik sebagai Pengetua Kolej Ibu Zain (KIZ) UKM pada 2002 sehingga kini. Di bawah kepimpinan beliau, KIZ telah berjaya ditransformasikan menjadikan sebuah Kolej Kediaman Premier yang pertama di kalangan IPTA. Seterusnya mulai Julai 2018, beliau yelah diberi kepercayaan untuk memimpin Pusat Aktiviti Pelajar UKM (PUSANIKA-UKM). Sepanjang perkhidmatan sebagai pengetua di KIZ, beliau banyak terlibat dengan pelbagai jawatankuasa Pembangunan Fizikal UKM. Sejak tahun 2017 hingga kini , beliau juga telah dilantik sebagai Panel Teknikal untuk penyediaan dokumen Pelan Induk Fizikal UKM.</p>



